import java.util.*;
public class Calculator {

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
//constant range for numbers 1 and 2
	final static int range1 = -1000000;
	final static int range2 = 1000000;
	final static double powerRaisedBy = 10;
	
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	public static void main(String[] args) {
	
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//Scanner so the user can type in inputs
		Scanner keyboard = new Scanner(System.in);
		
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
//Declared variables

		char addition ='+';
		char subtraction ='-';
		char multiplication ='*';
		char division ='/';
		char powerOf10 ='p';
		char multiplyByE ='e';
		char logBase10 ='l';
		double num1;
		double num2;
		double answer1;
		double answer2;
		char operations;
		
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
//Start of loop
do { 
		
//Welcome message	
		System.out.println("\nWelcome to Simple Calculator - circa 1970");
		
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
//Space for user to enter number 2 within a the range
		//Start of loop
		do { 
			
			System.out.print("\nPlease enter Number 1: ");
			num1 = keyboard.nextDouble();
		
			if (num1<range1 || num1>range2) {
				System.out.print("\nNumber 1 is outside allowed range: -1000000 -> 1000000\n");
			}//End of If
		
		}while (num1<range1 || num1>range2);
		//end of loop
		
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
//Space for user to enter number 2 within a the range
		//Start of loop
		do { 
			
			System.out.print("Please enter Number 2: ");
			num2 = keyboard.nextDouble();
			
			if (num2<range1 || num2>range2) {
				System.out.print("\nNumber 2 is outside allowed range: -1000000 -> 1000000\n");
			}//End of If
			
		}while (num2<range1 || num2>range2);
		//end of loop
		
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

//Option for user to select which operation to use
		System.out.print("Please enter the operation to be performed: (+ - * / p e l ):");
		operations = keyboard.next().charAt(0);
		
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
//Display for user repeating what they entered
		
		System.out.print("\nYou Entered Number 1 = "+num1);
		System.out.print("\nYou Entered Number 2 = "+num2);
		System.out.print("\nYou Entered Operation = "+operations+"\n");
		
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
//Operation if addition is selected
		if (operations == '+') {
			answer1 = num1 + num2;
			System.out.print("\n"+num1+" + "+num2+" = " +answer1+"\n");
		}//end of else If
		
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
//Operation if subtraction is selected
		else if (operations == '-') {
			answer1 = num1 - num2;
			System.out.print("\n"+num1+" - "+num2+" = " +answer1+"\n");
		}//end of else If
		
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
//Operation if multiplication is selected
		else if (operations == '*') {
			answer1 = num1 * num2;
			System.out.print("\n"+num1+" * "+num2+" = " +answer1+"\n");
		}//end of else If
		
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
//Operation if division is selected
		else if (operations == '/') {
			answer1= num1 / num2;
			System.out.print("\n"+num1+" / "+num2+" = " +answer1+"\n");
		}//end of else If
		
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
//Operation if the power of 10 is selected
		else if (operations == 'p') {
			answer1 = Math.pow(num1, powerRaisedBy);
			answer2 = Math.pow(num2, powerRaisedBy);
			System.out.println("\n"+num1 +" raised to "+powerRaisedBy+" power = "+ answer1);
			System.out.println(num2 +" raised to "+powerRaisedBy+" power = "+ answer2+"\n");
		}//end of else If
		
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
//Operation if multiply by e is selected
		else if (operations == 'e') {
			answer1 = num1 * Math.E;
			answer2 = num2 * Math.E;
			System.out.print("\n"+num1+" * "+Math.E+" = " +answer1+"\n");
			System.out.print(num2+" * "+Math.E+" = " +answer2+"\n");
		}//end of else If
		
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
//Operation if log base 10 is selected
		else if (operations == 'l') {
			answer1 = Math.log10(num1);
			answer2 = Math.log10(num2);
			System.out.print("\nLog("+num1+") = " +answer1+"\n");
			System.out.print("Log("+num2+") = " +answer2+"\n");
		}//end of else If
		
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
//invalid operation statement
		else {
			System.out.print("\nIllegal operation specified: Please Enter One Of The Operations Displayed\n\n");
		}//end of else

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
//Loop so that if the user enters a invalid operation the screen will reappear
}while (!(operations == '+') || !(operations == '-') || !(operations == '*') || !(operations == '/') || !(operations == 'p') || !(operations == 'e') || !(operations == 'l')); 

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
		
	}//end of main

}//end of class
